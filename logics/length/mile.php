<?php

function miToMm($length) {
    return $length * 1609344;
}
function miToCm($length) {
    return $length * 160934;
}
function miToM($length) {
    return $length * 1609.344;
}
function miToKm($length) {
    return $length * 1.60934;
}
function miToInch($length) {
    return $length * 63360;
}
function miToFoot($length) {
    return $length * 5280;
}
function miToYard($length) {
    return $length * 1760;
}
