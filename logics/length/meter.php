<?php

function mToMm($length) {
    return $length * 1000;
}
function mToCm($length) {
    return $length * 100;
}
function mToM($length) {
    return $length;
}
function mToKm($length) {
    return $length / 1000;
}
function mToInch($length) {
    return $length * 39.3700787;
}
function mToFoot($length) {
    return $length * 3.2808399;
}
function mToYard($length) {
    return $length * 1.0936133;
}
function mToMile($length) {
    return $length / 1609.344;
}
