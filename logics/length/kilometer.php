<?php

function kmToMm($length) {
    return $length * 1000000;
}
function kmToCm($length) {
    return $length * 100000;
}
function kmToM($length) {
    return $length * 1000;
}
function kmToInch($length) {
    return $length * 39370.0787;
}
function kmToFoot($length) {
    return $length * 3280.8399;
}
function kmToYard($length) {
    return $length * 1093.6133;
}
function kmToMile($length) {
    return $length / 1.609344;
}
