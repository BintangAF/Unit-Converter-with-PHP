<?php

function inchToMm($length) {
    return $length * 25.4;
}
function inchToCm($length) {
    return $length * 2.54;
}
function inchToM($length) {
    return $length / 39.37;
}
function inchToKm($length) {
    return $length / 39370.0787;
}
function inchToFoot($length) {
    return $length / 12;
}
function inchToYard($length) {
    return $length / 36;
}
function inchToMile($length) {
    return $length / 63360;
}
