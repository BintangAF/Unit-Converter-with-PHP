<?php
    function mmToCm($length) {
        return $length / 10;
    }

    function mmToM($length) {
        return $length / 1000;
    }

    function mmToKm($length) {
        return $length / 1000000;
    }

    function mmToInch($length) {
        return $length / 25.4;
    }

    function mmToFoot($length) {
        return $length / 304.8;
    }

    function mmToYard($length) {
        return $length / 914.4;
    }

    function mmToMile($length) {
        return $length / 1609344;
    }
