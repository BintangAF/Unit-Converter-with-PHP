<?php

function ydToMm($length) {
    return $length * 914.4;
}
function ydToCm($length) {
    return $length * 91.44;
}
function ydToM($length) {
    return $length * 0.9144;
}
function ydToKm($length) {
    return $length / 1093.6133;
}
function ydToInch($length) {
    return $length * 36;
}
function ydToFoot($length) {
    return $length * 3;
}
function ydToMile($length) {
    return $length / 1760;
}
