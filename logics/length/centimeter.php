<?php

function cmToMm($length) {
    return $length * 10;
}
function cmToM($length) {
    return $length / 100;
}
function cmToKm($length) {
    return $length / 100000;
}
function cmToInch($length) {
    return $length / 2.54;
}
function cmToFoot($length) {
    return $length / 30.48;
}
function cmToYard($length) {
    return $length / 91.44;
}
function cmToMile($length) {
    return $length / 160934.4;
}